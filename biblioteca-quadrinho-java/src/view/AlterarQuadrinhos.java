    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if(camposObrigatoriosOk()){
            try{
                try {
                    TelaLogin.funcao.alterarQuadrinho(this.quadrinho.getNotaFiscal().getValor(),
                            pegarUrl(), txtAutor.getText(), Integer.parseInt(txtQtdPaginas.getText()),               
                            pegarSubtitulo(),this.quadrinho.getNotaFiscal().getDataCompra(), pegarSinopse(), txtTitulo.getText(), pegarIdColecao());
                
                    JOptionPane.showMessageDialog(this, "Quadrinho alterado com sucesso");
                    new Quadrinhos().setVisible(true);
                    this.dispose();
                } catch (ParseException ex) {
                    JOptionPane.showMessageDialog(this, "Insira uma data no formato correto");
                }
                
            }catch(NumberFormatException  e){
                JOptionPane.showMessageDialog(this, "Insira um valor numérico válido");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios");
        }
   
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private Integer pegarIdColecao(){
        String colecao = txtColecao.getSelectedItem().toString();
        if(!colecao.equals("NULL")){
            ColecaoController colecaoC = new ColecaoController();
            List<Colecao> colecoes = colecaoC.listar();
            for(Colecao c: colecoes){
                if(c.getNome().equals(colecao)){
                    return c.getId();
                }
            }
        }
        
        return -1;
    }
    
    private String pegarSinopse(){
        if(txtSinopse.getText().equals(""))
            return null;
        return txtSinopse.getText();
    }
    
    private String pegarSubtitulo(){
        if(txtSubtitulo.getText().equals(""))
            return null;
        return txtSubtitulo.getText();
    }
    
    private String pegarUrl(){
        if(txtUrl.getText().equals(""))
           return null;
        return txtUrl.getText();
    }
    
    private boolean camposObrigatoriosOk() {
        
        if(txtTitulo.getText().equals(""))
            return false;
        
        if(txtAutor.getText().equals(""))
            return false;
        if(txtQtdPaginas.getText().equals(""))
            return false;
        
        return true;
    }

