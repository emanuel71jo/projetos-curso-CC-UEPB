    public AlterarQuadrinhos(int linha) {
        initComponents();
        this.linhaSelecionada = linha;
        ColecaoController colecaoC = new ColecaoController();
        List<Colecao> colecoes = colecaoC.listar();
        
        colecoes.forEach((c) -> {
            txtColecao.addItem(c.getNome());
        });
        
        QuadrinhoController quadrinhoC = new QuadrinhoController();
        List<Quadrinho> quadrinhos = quadrinhoC.listar();
        
        int cont = 0;
        for(Quadrinho q: quadrinhos){
            if(cont == linha){
                this.quadrinho = q;
                break;
            }
            cont++;
        }
        txtTitulo.setText(this.quadrinho.getTitulo());
        txtUrl.setText(this.quadrinho.getUrlCapa());
        txtAutor.setText(this.quadrinho.getAutor());
        txtQtdPaginas.setText(String.valueOf(this.quadrinho.getQtdPaginas()));
        txtSubtitulo.setText(this.quadrinho.getSubtitulo());
        txtSinopse.setText(this.quadrinho.getSinopse()); 
    }
    
