    public void atualizarTabela(){
        DefaultTableModel tTabela = (DefaultTableModel) jTable1.getModel();
        tTabela.setNumRows(0);        
        List<Colecao> colecoes = TelaLogin.funcao.listarColecao();
        
        
        for(Colecao c: colecoes){           
            
            Object[] obj = {c.getId(), c.getNome(), c.getEditora()};
            
            tTabela.addRow(obj);
            
            
        }
        
        jTable1.setModel(tTabela);
        
    }

