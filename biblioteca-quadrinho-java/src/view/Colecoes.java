    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int linhaSelecionada = jTable1.getSelectedRow();
 
        if (linhaSelecionada >= 0){
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir a colecao?");
            if (resposta == JOptionPane.YES_OPTION){
                List<Colecao> colecoes = TelaLogin.funcao.listarColecao();
                int cont = 0, id = -1;
                for(Colecao c: colecoes){
                    if(cont == linhaSelecionada){
                        id = c.getId();
                        break;
                    }
                    cont++;
                }
                TelaLogin.funcao.removerColecao(id);
                atualizarTabela();
            }else{
                JOptionPane.showMessageDialog(this, "Coleção não excluida", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "É necessário selecionar uma coleção");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

