    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = jTable1.getSelectedRow();
        if(linha >= 0){
            List<Emprestimo> emprestimos = TelaLogin.funcao.listarEmprestimo();
            int cont = 0;
            for(Emprestimo e: emprestimos){
                if(cont == linha){
                    if(e.getDataDevolucao() == null){
                        TelaLogin.funcao.informaDevolucaoEmprestimo(e.getHoraEmprestimo(), e.getQuadrinho().getId());
                        atualizarTabela();
                        break;
                    }else
                        JOptionPane.showMessageDialog(this, "Quadrinho selecionado já devolvido");
                }
                cont++;
            }
            
            
        }else
            JOptionPane.showMessageDialog(this, "Selecione o quadrinho para realizar devolução");
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int linha = jTable1.getSelectedRow();
        
        if(linha >= 0){
            List<Emprestimo> emprestimos = TelaLogin.funcao.listarEmprestimo();
            int cont = 0;
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja cancelar o emprestimo?");
            if (resposta == JOptionPane.YES_OPTION){
                for(Emprestimo e: emprestimos){
                    if(cont == linha){
                        if(e.getDataDevolucao() == null){
                            TelaLogin.funcao.cancelaEmprestimo(e.getHoraEmprestimo(), e.getQuadrinho().getId());
                        }else
                            JOptionPane.showMessageDialog(this, "Não pode cancelar um emprestimo que já foi devolvido");
                        atualizarTabela();
                        break;
                    }
                    cont++;
                }    
            }else{
                JOptionPane.showMessageDialog(this, "Emprestimo não cancelado", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
 
        }else
            JOptionPane.showMessageDialog(this, "Selecione um empréstimo");
    }//GEN-LAST:event_jButton4ActionPerformed

