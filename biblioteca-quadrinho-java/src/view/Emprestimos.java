    public void atualizarTabela(){
        DefaultTableModel tTabela = (DefaultTableModel) jTable1.getModel();
        tTabela.setNumRows(0);        
        List<Emprestimo> emprestimos = TelaLogin.funcao.listarEmprestimo();
        
        
        for(Emprestimo e: emprestimos){           
            
            Object[] obj = {e.getHoraEmprestimo(), e.getDataDevolucao(), e.getQuadrinho().getTitulo(),
                            e.getPessoa().getNome(), e.getUsuario().getNome()};
            
            tTabela.addRow(obj);
            
            
        }
        
        jTable1.setModel(tTabela);
        
    }

