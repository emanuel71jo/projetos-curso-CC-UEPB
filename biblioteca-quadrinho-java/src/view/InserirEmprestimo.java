    public void atualizarTabela(){
        DefaultTableModel tTabela = (DefaultTableModel) jTable1.getModel();
        tTabela.setNumRows(0);        
        List<Quadrinho> quadrinhos = TelaLogin.funcao.listarQuadrinho();
        
        
        for(Quadrinho q: quadrinhos){           
            
            if(q.isStatus()){
                Object[] obj = {q.getId(), q.getTitulo(), q.getSubtitulo(), q.getQtdPaginas()};
                tTabela.addRow(obj);
            }          
            
        }
        
        jTable1.setModel(tTabela);
        
    }

