    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = jTable1.getSelectedRow();
        
        if(linha >= 0){
            List<Quadrinho> quadrinhos = TelaLogin.funcao.listarQuadrinho();
            int cont = 0;
            for(Quadrinho q: quadrinhos){           
                if(q.isStatus()){
                    if(cont == linha){
                        TelaLogin.funcao.realizarEmprestimo(TelaLogin.funcao.getUsuario().getCpf(), pegarCpfPessoa(), q.getId());
                        JOptionPane.showMessageDialog(this, "Empréstimo realizado com sucesso");
                        new Emprestimos().setVisible(true);
                        this.dispose();
                    }
                    cont++;
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione o quadrinho que deseja emprestar");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public String pegarCpfPessoa(){
        String nome = jComboBox1.getSelectedItem().toString();
        
        List<Pessoa> pessoas = new PessoaController().listar();
        
        for(Pessoa p: pessoas){
            if(p.getNome().equals(nome))
                return p.getCpf();
        }
        
        return null;
    }
    
