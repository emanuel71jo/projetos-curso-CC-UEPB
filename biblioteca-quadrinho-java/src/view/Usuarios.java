    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();
 
        if (linhaSelecionada >= 0){
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir o usuário?");
            if (resposta == JOptionPane.YES_OPTION){
                List<Usuario> usuarios = TelaLogin.funcao.listarUsuario();
                int cont = 0;
                String cpf = "";
                for(Usuario u: usuarios){
                    if(cont == linhaSelecionada){
                        cpf = u.getCpf();
                        break;
                    }
                    cont++;
                }
                TelaLogin.funcao.removerUsuario(cpf);
                atualizarTabela();
            }else{
                JOptionPane.showMessageDialog(this, "Usuário não excluido", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "É necessário selecionar um usuário");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

