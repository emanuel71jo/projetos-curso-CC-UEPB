    public void atualizarTabela(){
        DefaultTableModel tTabela = (DefaultTableModel) jTable1.getModel();
        tTabela.setNumRows(0);        
        List<Usuario> usuarios = TelaLogin.funcao.listarUsuario();
        
        
        for(Usuario u: usuarios){           
            String permissao = "Usuario";
            
            if(u.isPermissao())
                permissao = "Administrador";
            
            Object[] obj = {u.getCpf(), u.getNome(), u.getLogin(), permissao};
            
            tTabela.addRow(obj);
            
            
        }
        
        jTable1.setModel(tTabela);
        
    }

