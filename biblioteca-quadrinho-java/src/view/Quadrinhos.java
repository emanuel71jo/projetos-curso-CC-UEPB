    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();
 
        if (linhaSelecionada >= 0){
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir o quadrinho?");
            if (resposta == JOptionPane.YES_OPTION){
                List<Quadrinho> quadrinhos = TelaLogin.funcao.listarQuadrinho();
                int cont = 0, id = -1;
                for(Quadrinho q: quadrinhos){
                    if(cont == linhaSelecionada){
                        id = q.getId();
                        break;
                    }
                    cont++;
                }
                
                TelaLogin.funcao.removerQuadrinho(id);
                atualizarTabela();
            }else{
                JOptionPane.showMessageDialog(this, "É necessário selecionar um quadrinho", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

