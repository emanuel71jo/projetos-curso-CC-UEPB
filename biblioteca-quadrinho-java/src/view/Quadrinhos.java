    public void atualizarTabela(){
        DefaultTableModel tTabela = (DefaultTableModel) jTable1.getModel();
        tTabela.setNumRows(0);
        QuadrinhoController quadrinhoC = new QuadrinhoController();
        
        List<Quadrinho> quadrinho = quadrinhoC.listar();
        
        
        for(Quadrinho q: quadrinho){           
            String status;
            if(q.isStatus())
                status = "Disponível";
            else
                status = "Indispoível";
            
            
            Object[] obj = {q.getId(), q.getTitulo(), q.getSubtitulo(), q.getAutor(), q.getQtdPaginas(),
                q.getNotaFiscal().getValor(), q.getNotaFiscal().getDataCompra(), q.getSinopse(), q.getUrlCapa(),
                q.getColecao().getNome(), status};
            
            tTabela.addRow(obj);
            
            
        }
        
        jTable1.setModel(tTabela);
        
    }

