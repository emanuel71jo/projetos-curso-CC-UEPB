    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(camposOk()){
            TelaLogin.funcao.alterarUsuario(TelaLogin.funcao.getUsuario().getCpf(),
                    txtNome.getText(), TelaLogin.funcao.getUsuario().isTipoPessoa(),
                    txtLogin.getText(), new String(txtSenha.getPassword()), 
                    TelaLogin.funcao.getUsuario().isPermissao());
            JOptionPane.showMessageDialog(this, "Usu치rio alterado com sucesso/ Necess치rio logar novamente");
            new TelaLogin().setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Preencha todos os campos necess치rios");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public boolean camposOk(){
        if(txtNome.getText().equals(""))
            return false;
        if(txtLogin.getText().equals(""))
            return false;
        if(new String(txtSenha.getPassword()).equals(""))
            return false;
        return true;
    }
    
