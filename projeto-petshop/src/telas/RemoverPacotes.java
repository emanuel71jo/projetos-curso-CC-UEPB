    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
            int op = jTable1.getSelectedRow(), i = 0;
            
            dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setNumRows(0);
            if(op >= 0){

                for(SuperClasse s: sc){
                    if(s instanceof Pacotes){
                        if(i == op){
                            sc.remove(s);
                            gere.cadastrar(sc);
                            JOptionPane.showMessageDialog(this, "PACOTE REMOVIDO COM SUCESSO",
                                "REMOVIDO", JOptionPane.WARNING_MESSAGE);
                          
                            for(SuperClasse ss: sc){
                                if(ss instanceof Pacotes){
                                    Object[] ob = {ss.getCod(),((Pacotes) ss).getDescricao(),((Pacotes) ss).getPreco()};
                                    dtm.addRow(ob);
                                }
                            }
                            jTable1.setModel(dtm);
                        }

                        i++;
                    }
                }

            }else{
                JOptionPane.showMessageDialog(this, "SELECIONE UM PACOTE", "ERRO", JOptionPane.ERROR_MESSAGE);

            }
        }catch(ConcurrentModificationException e){

        }
    }//GEN-LAST:event_jButton1ActionPerformed

