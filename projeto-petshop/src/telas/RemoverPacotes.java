    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int op = jTable1.getSelectedRow(), i = 0;
        dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setNumRows(0);
        if(op >= 0){
            for(SuperClasse s: sc){
                if(s instanceof Pacotes){
                    if(i == op){
                        for(SuperClasse ss: ((Pacotes) s).getItens()){
                            Object[] ob = {ss.getCod(), ((Servicos)ss).getDescricao(),((Servicos)ss).getPreco()};
                            dtm.addRow(ob);
                        }
                        jTable2.setModel(dtm);  
                    }
                    i++;
                }
  
            }
            
            
        }else{
            
            JOptionPane.showMessageDialog(this, "SELECIONE UM PACOTE", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

