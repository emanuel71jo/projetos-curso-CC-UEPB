    public LojaCarrinho(int cod) {
        initComponents();
        this.cod = cod;
        dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setNumRows(0);
        for(SuperClasse s: sc){
            if(s instanceof Cliente){
                if(cod == s.getCod()){
                    for(SuperClasse ss: ((Cliente) s).getCarrinho()){
                        if(ss instanceof Produtos){
                            Object[] ob = {"Produto", ((Produtos) ss).getDescricao(),
                                ((Produtos) ss).getPreco()};
                            dtm.addRow(ob);
                        }else if(ss instanceof Servicos){
                            Object[] ob = {"Serviços",((Servicos) ss).getDescricao(),((Servicos) ss).getPreco()};
                            dtm.addRow(ob);
                        }else{
                            Object[] ob = {"Pacotes",((Pacotes)ss).getDescricao(),((Pacotes)ss).getPreco()};
                            dtm.addRow(ob);
                        }
                        
                    }
                    
                    break;
                }
            }
        }
        jTable1.setModel(dtm);
    }

