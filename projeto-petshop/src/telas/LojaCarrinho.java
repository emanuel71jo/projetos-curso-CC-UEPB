    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int cont = 0;
        
        for(SuperClasse s: sc){
            if(s instanceof Cliente){
                if(s.getCod() == cod){
                    ArrayList<SuperClasse> aux = ((Cliente) s).getCarrinho();
                    for(SuperClasse j: aux){
                        if(j instanceof Produtos){
                            for(SuperClasse k: sc){
                                if(j.getCod() == k.getCod()){
                                    ((Produtos)k).setQtd(((Produtos)k).getQtd() + 1);
                                }
                            }
                            
                        }                          
                    }
                    aux.clear();
                    ((Cliente) s).setCarrinho(aux);
                    break;
                }
            }
        }
       
        gere.cadastrar(sc);
        dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setNumRows(0);
        jTable1.setModel(dtm);
        this.dispose();
        new MenuLoja(cod).setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

