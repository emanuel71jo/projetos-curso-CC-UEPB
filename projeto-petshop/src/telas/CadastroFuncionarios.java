    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if(jTextField1.getText().equals("") || new String(jFormattedTextField1.getText().toString()).equals("")){
            JOptionPane.showMessageDialog(this, "PREENCHA TODOS OS CAMPOS", "ERRO", JOptionPane.ERROR_MESSAGE);
        }else{
            sc = gere.listar();
            int cod = 1000, tam = sc.size() - 1, aux = 0;
            for(SuperClasse s: sc){
                if(tam == aux){
                    cod = s.getCod() + 1;
                }
                aux++;
            }
            sc.add(new Funcionario(cod, jTextField1.getText(), new String(jFormattedTextField1.getText().toString())));
            gere.cadastrar(sc);
            JOptionPane.showMessageDialog(this, "CADASTRO COM SUCESSO", "OK", JOptionPane.OK_CANCEL_OPTION);
            new MenuCadastro().setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

